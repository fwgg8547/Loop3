package com.fwgg8547.loop2.gamebase.sequencerbase;

public class ScaleSequence
{
	public int frame;
	public float scalex;
	public float scaley;

	public ScaleSequence(){}
	
	public ScaleSequence(int frame, float scalex, float scaley){
		this.frame = frame;
		this.scalex = scalex;
		this.scaley = scaley;
	}
}
